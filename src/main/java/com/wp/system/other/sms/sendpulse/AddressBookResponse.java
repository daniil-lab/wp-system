package com.wp.system.other.sms.sendpulse;

public class AddressBookResponse {

    private int id;

    private String name;

    public AddressBookResponse() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
